#include"libft.h"

int	ft_isalnum(int alnum)
{
	if ((alnum >= 48 && alnum <= 57)
		|| (alnum >= 65 && alnum <= 90) || (alnum >= 97 && alnum <= 122))
		return (1);
	else
		return (0);
}
